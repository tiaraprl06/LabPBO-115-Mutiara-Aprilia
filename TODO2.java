package TugasLabPBO1;

public class TODO2 {
    public static void main(String[] args) {

            // Menampilkan nilai π
            System.out.println("Nilai π: " + Math.PI);

            // Menghitung akar kuadrat dari 9
            System.out.println("Akar kuadrat dari 9: " + Math.sqrt(9));

            // Menghitung pangkat dua dari 5
            System.out.println("Pangkat dua dari 5: " + Math.pow(5, 2));

            // Membulatkan 3.14 ke atas
            System.out.println("Membulatkan 3.14 ke atas: " + Math.ceil(3.14));

            // Membulatkan 3.14 ke bawah
            System.out.println("Membulatkan 3.14 ke bawah: " + Math.floor(3.14));

            // Menghitung nilai maksimum dari 10 dan 20
            System.out.println("Nilai maksimum dari 10 dan 20: " + Math.max(10, 20));

            // Menghitung nilai minimum dari 10 dan 20
            System.out.println("Nilai minimum dari 10 dan 20: " + Math.min(10, 20));

            // Menghitung nilai absolut dari -5
            System.out.println("Nilai absolut dari -5: " + Math.abs(-5));

            // Menghitung nilai random antara 0 dan 1
            System.out.println("Nilai random antara 0 dan 1: " + Math.random());
    }

}
